#include "tools.h"

size_t	ft_atoi(char *str)
{
	int		i;
	size_t	nb;
	int		inf;

	inf = 0;
	nb = 0;
	i = 0;
	if (str[i] == '+')
		i++;
	if (!(str[i] >= '0' && str[i] <= '9'))
		return (0);
	while (str[i] >= '0' && str[i] <= '9')
		nb = nb * 10 + (str[i++] - '0');
	if (nb <= 2147483647)
		return (nb);
	return (0);
}
